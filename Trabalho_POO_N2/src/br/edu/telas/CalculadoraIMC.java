    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
        if (txtPeso.getText().contains(",")) {
            peso = Double.parseDouble(txtPeso.getText().replace(",", "."));
        } else {
            peso = Double.parseDouble(txtPeso.getText());
        } if (txtAltura.getText().contains(",")) {
            altura = Double.parseDouble(txtAltura.getText().replace(",", "."));
        } else {
            altura = Double.parseDouble(txtAltura.getText());
        } imc = peso/ (altura*altura);
        
        if (altura == 0) {
            txtResultado.setText("Digite um valor diferente de 0!");
        } else {
            if(imc < 17){
                txtResultado.setText(df.format(imc) + " - Muito abaixo do peso");
            }
            else if(imc >= 17 && imc <=18.49){
                txtResultado.setText(df.format(imc) + " - Abaixo do peso");
            }
            else if(imc >= 18.5 && imc<= 24.99){
                txtResultado.setText(df.format(imc) + " - Peso normal");
            }
            else if(imc >= 25 && imc<= 29.99){
                txtResultado.setText(df.format(imc) + " - Acima do peso");
            }
            else if(imc >= 30 && imc<= 34.99){
                txtResultado.setText(df.format(imc) + " - Obesidade I");
           }
            else if(imc >= 35 && imc<= 39.99){
                txtResultado.setText(df.format(imc) + " - Obesidade II");
            }
            else {
                txtResultado.setText(df.format(imc) + " - Obesidade MÃ³rbida");
            }
        }
    }//GEN-LAST:event_CalcularActionPerformed

